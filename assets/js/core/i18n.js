export function updateLanguageButtonLabel() {
  const btn = document.getElementById('changeLanguageBtn');
  if (!btn) return;
  const label = btn.querySelector('span:last-child');
  if (!label) return;
  const lang = getLanguage();
  label.textContent = `${t('settings.changeLanguage', {}, lang)} · ${lang === 'th' ? t('settings.languageThai', {}, lang) : t('settings.languageEnglish', {}, lang)}`;
}

export function applyTranslations(page = document.body?.dataset?.page || '') {
  const lang = getLanguage();
  state.currentLanguage = lang;
  applyCommon(lang);
  applyRules(PAGE_RULES[page] || [], lang);
  updateLanguageButtonLabel();
  document.dispatchEvent(new CustomEvent('resident-language-applied', { detail: { lang, page } }));
}

function ensurePicker() {
  let picker = document.getElementById('residentLanguagePicker');
  if (picker) return picker;
  picker = document.createElement('div');
  picker.id = 'residentLanguagePicker';
  picker.className = 'resident-language-picker hidden';
  picker.innerHTML = `
    <div class="resident-language-picker__backdrop" data-lang-close="1"></div>
    <div class="resident-language-picker__dialog" role="dialog" aria-modal="true" aria-labelledby="residentLanguagePickerTitle">
      <div class="resident-language-picker__eyebrow">ELITE BLACK CARD</div>
      <h3 id="residentLanguagePickerTitle"></h3>
      <p id="residentLanguagePickerSubtitle" class="resident-language-picker__subtitle"></p>
      <div class="resident-language-picker__current">
        <span>${t('settings.currentLanguage')}</span>
        <strong id="residentLanguageCurrentValue"></strong>
      </div>
      <div class="resident-language-picker__options">
        <button type="button" class="resident-language-picker__option" data-lang-option="en">English</button>
        <button type="button" class="resident-language-picker__option" data-lang-option="th">ไทย</button>
      </div>
      <button type="button" class="resident-language-picker__cancel" data-lang-close="1"></button>
    </div>
  `;
  document.body.appendChild(picker);
  picker.querySelectorAll('[data-lang-close="1"]').forEach((node) => {
    node.addEventListener('click', () => picker.classList.add('hidden'));
  });
  picker.querySelectorAll('[data-lang-option]').forEach((node) => {
    node.addEventListener('click', () => {
      const selected = node.getAttribute('data-lang-option') || 'en';
      setLanguage(selected);
      window.location.reload();
    });
  });
  return picker;
}

export function openLanguagePicker() {
  const lang = getLanguage();
  const picker = ensurePicker();
  picker.querySelector('#residentLanguagePickerTitle').textContent = t('settings.pickerTitle', {}, lang);
  picker.querySelector('#residentLanguagePickerSubtitle').textContent = t('settings.pickerSubtitle', {}, lang);
  picker.querySelector('#residentLanguageCurrentValue').textContent = lang === 'th' ? t('settings.languageThai', {}, lang) : t('settings.languageEnglish', {}, lang);
  picker.querySelector('.resident-language-picker__cancel').textContent = t('settings.cancel', {}, lang);
  picker.querySelectorAll('[data-lang-option]').forEach((button) => {
    const code = button.getAttribute('data-lang-option');
    button.classList.toggle('is-active', code === lang);
    button.textContent = code === 'th' ? t('settings.languageThai', {}, lang) : t('settings.languageEnglish', {}, lang);
  });
  picker.classList.remove('hidden');
}
