function deepGet(obj, path) {
  return String(path || '').split('.').reduce((acc, part) => (acc && acc[part] != null ? acc[part] : undefined), obj);
}

function formatString(value, params = {}) {
  return String(value || '').replace(/\{(\w+)\}/g, (_, key) => params[key] != null ? String(params[key]) : '');
}

export function getLanguage() {
  const stored = String(localStorage.getItem(STORAGE_KEY) || '').trim().toLowerCase();
  if (SUPPORTED.has(stored)) return stored;
  return 'en';
}

export function setLanguage(lang = 'en') {
  const safe = SUPPORTED.has(String(lang).toLowerCase()) ? String(lang).toLowerCase() : 'en';
  localStorage.setItem(STORAGE_KEY, safe);
  state.currentLanguage = safe;
  document.documentElement.lang = safe === 'th' ? 'th' : safe === 'ru' ? 'ru' : safe === 'zh' ? 'zh-CN' : 'en';
  return safe;
}

export function initLanguage() {
  return setLanguage(getLanguage());
}

export function t(key, params = {}, lang = getLanguage()) {
  const value = deepGet(DICT[lang], key) ?? deepGet(DICT.en, key) ?? key;
  return formatString(value, params);
}

function setNodeValue(node, value, attr = 'text') {
  if (!node) return;
  if (attr === 'html') {
    node.innerHTML = value;
  } else if (attr === 'placeholder') {
    node.setAttribute('placeholder', value);
  } else if (attr === 'title') {
    node.setAttribute('title', value);
  } else if (attr === 'aria-label') {
    node.setAttribute('aria-label', value);
  } else if (attr === 'text') {
    node.textContent = value;
  }
}

function applyRules(rules = [], lang = getLanguage()) {
  rules.forEach((rule) => {
    const nodes = Array.from(document.querySelectorAll(rule.selector || ''));
    if (!nodes.length) return;
    const node = Number.isInteger(rule.index) ? nodes[(rule.index || 1) - 1] : null;
    const targets = node ? [node] : nodes;
    const value = rule.key ? t(rule.key, {}, lang) : (lang === 'th' ? rule.th : rule.en);
    targets.forEach((target) => setNodeValue(target, value, rule.attr || 'text'));
  });
}

function applyCommon(lang = getLanguage()) {
  document.documentElement.lang = lang === 'th' ? 'th' : lang === 'ru' ? 'ru' : lang === 'zh' ? 'zh-CN' : 'en';
  applyRules([
    { selector: '#residentRouteLoader .resident-route-loader__caption', key: 'common.loadingResidentExperience' },
    { selector: '#residentLoginBtn', key: 'common.logIn' },
    { selector: '#logoutBtn', key: 'common.logOut' },
    { selector: '.vault-appbar-back', attr: 'aria-label', key: window.location.pathname.includes('settings') ? 'common.backToHome' : 'common.backToSettings' },
    { selector: '.vault-bottom-nav [data-page-link="home"] .nav-label', key: 'common.home' },
    { selector: '.vault-bottom-nav [data-page-link="redemption"] .nav-label', key: 'common.redemption' },
    { selector: '.vault-bottom-nav [data-page-link="member"] .nav-label', key: 'common.member' },
    { selector: '.vault-bottom-nav [data-page-link="settings"] .nav-label', key: 'common.settings' },
  ], lang);
}

export function updateLanguageButtonLabel() {
  const btn = document.getElementById('changeLanguageBtn');
  if (!btn) return;
  const label = btn.querySelector('span:last-child');
  if (!label) return;
  const lang = getLanguage();
  label.textContent = `${t('settings.changeLanguage', {}, lang)} · ${languageLabel(lang, lang)}`;
}

export function applyTranslations(page = document.body?.dataset?.page || '') {
  const lang = getLanguage();
  state.currentLanguage = lang;
  applyCommon(lang);
  applyRules(PAGE_RULES[page] || [], lang);
  updateLanguageButtonLabel();
  document.dispatchEvent(new CustomEvent('resident-language-applied', { detail: { lang, page } }));
}

function ensurePicker() {
  let picker = document.getElementById('residentLanguagePicker');
  if (picker) return picker;
  picker = document.createElement('div');
  picker.id = 'residentLanguagePicker';
  picker.className = 'resident-language-picker hidden';
  picker.innerHTML = `
    <div class="resident-language-picker__backdrop" data-lang-close="1"></div>
    <div class="resident-language-picker__dialog" role="dialog" aria-modal="true" aria-labelledby="residentLanguagePickerTitle">
      <div class="resident-language-picker__eyebrow">ELITE BLACK CARD</div>
      <h3 id="residentLanguagePickerTitle"></h3>
      <p id="residentLanguagePickerSubtitle" class="resident-language-picker__subtitle"></p>
      <div class="resident-language-picker__current">
        <span>${t('settings.currentLanguage')}</span>
        <strong id="residentLanguageCurrentValue"></strong>
      </div>
      <div class="resident-language-picker__options">
        <button type="button" class="resident-language-picker__option" data-lang-option="en">English</button>
        <button type="button" class="resident-language-picker__option" data-lang-option="th">ไทย</button>
        <button type="button" class="resident-language-picker__option" data-lang-option="ru">Русский</button>
        <button type="button" class="resident-language-picker__option" data-lang-option="zh">中文</button>
      </div>
      <button type="button" class="resident-language-picker__cancel" data-lang-close="1"></button>
    </div>
  `;
  document.body.appendChild(picker);
  picker.querySelectorAll('[data-lang-close="1"]').forEach((node) => {
    node.addEventListener('click', () => picker.classList.add('hidden'));
  });
  picker.querySelectorAll('[data-lang-option]').forEach((node) => {
    node.addEventListener('click', () => {
      const selected = node.getAttribute('data-lang-option') || 'en';
      setLanguage(selected);
      window.location.reload();
    });
  });
  return picker;
}

export function openLanguagePicker() {
  const lang = getLanguage();
  const picker = ensurePicker();
  picker.querySelector('#residentLanguagePickerTitle').textContent = t('settings.pickerTitle', {}, lang);
  picker.querySelector('#residentLanguagePickerSubtitle').textContent = t('settings.pickerSubtitle', {}, lang);
  picker.querySelector('#residentLanguageCurrentValue').textContent = languageLabel(lang, lang);
  picker.querySelector('.resident-language-picker__cancel').textContent = t('settings.cancel', {}, lang);
  picker.querySelectorAll('[data-lang-option]').forEach((button) => {
    const code = button.getAttribute('data-lang-option');
    button.classList.toggle('is-active', code === lang);
    button.textContent = languageLabel(code, lang);
  });
  picker.classList.remove('hidden');
}
